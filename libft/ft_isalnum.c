int	ft_isalnum(int c)
{
	if ((c > 47 && c < 58) || (c > 64 && c < 91)
		|| (c > 96 && c < 123))
		return (1);
	return (0);
}
/*The isalnum() function returns a non-zero value if the character passed as 
an int parameter is alphabetical or a decimal digit character.*/